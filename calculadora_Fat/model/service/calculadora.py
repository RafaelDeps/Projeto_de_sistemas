#funcoes basicas de uma calculadora
class Calculadora:
    def somar (self, valor_1:int ,valor_2:int ):
        return valor_1 + valor_2
    def subtrair (self, valor_1:int ,valor_2:int ):
        return valor_1 - valor_2
    def multiplicar (self, valor_1:int ,valor_2:int ):
        return valor_1 * valor_2
    def dividir (self, valor_1:int ,valor_2:int ):
        return valor_1 / valor_2 
